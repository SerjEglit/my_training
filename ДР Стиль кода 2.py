# Исходный список
my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# Начальный индекс
index = 0

# Цикл while
while index < len(my_list):
	# Если число отрицательное, прерываем цикл
	if my_list[index] < 0:
		break
	# Если число равно 0, пропускаем текущую итерацию
	elif my_list[index] == 0:
		index += 1
		continue
	# Если число положительное, выводим его
	print(my_list[index])

	# Переходим к следующему индексу
	index += 1

# Объяснение работы программы по шагам:

# Инициализация:
# Исходный список my_list содержит заданные числа.
# Переменная index устанавливается в 0 для отслеживания текущего индекса.

# Цикл while:
# Цикл проверяет, чтобы index оставался в пределах списка (index < len(my_list)).

# Логика внутри цикла:
# Если текущий элемент списка отрицательный (my_list[index] < 0), цикл завершается с помощью break.
# Если текущий элемент равен 0, пропускаем обработку этого элемента и переходим к следующему с помощью continue.
# Если текущий элемент положительный, выводим его на экран.

# Инкремент индекса:
# После обработки текущего элемента увеличиваем index на 1.

# Завершение цикла:
# Цикл завершается, если:
# Достигнут отрицательный элемент.
# Список полностью пройден.
