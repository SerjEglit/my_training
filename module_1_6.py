# Работа со словарями и множествами

# Создание словаря
my_dict = {
    "Иван": 1995,
    "Мария": 2000,
    "Алексей": 1987
}

# Вывод словаря на экран
print("Словарь my_dict:", my_dict)

# Получение значения по существующему ключу
existing_key_value = my_dict.get("Иван")
print("Значение по ключу 'Иван':", existing_key_value)

# Получение значения по отсутствующему ключу (без ошибки)
non_existing_key_value = my_dict.get("Петр", "Ключ отсутствует")
print("Значение по отсутствующему ключу 'Петр':", non_existing_key_value)

# Добавление новых пар ключ-значение
my_dict["Светлана"] = 1993
my_dict["Дмитрий"] = 1989
print("Словарь после добавления пар:", my_dict)

# Удаление одной пары по существующему ключу
removed_value = my_dict.pop("Мария", "Ключ отсутствует")
print("Удаленное значение по ключу 'Мария':", removed_value)

# Вывод обновленного словаря
print("Словарь после удаления пары:", my_dict)

# Работа с множествами

# Создание множества с повторяющимися значениями
my_set = {1, 2, 2, 3, "apple", "banana", "apple", 4.5}

# Вывод множества (уникальные значения)
print("Множество my_set:", my_set)

# Добавление новых элементов
my_set.add("cherry")
my_set.add(10)
print("Множество после добавления элементов:", my_set)

# Удаление одного элемента
my_set.discard("banana")  # Используем discard, чтобы избежать ошибки, если элемента нет
print("Множество после удаления элемента 'banana':", my_set)
